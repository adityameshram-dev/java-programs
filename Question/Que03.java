// Write a program using sqrt( ) and pow( ) to calculate the square root and power of given number.


public class Que03 {
    public static void main(String[] args) {
        System.out.println(Math.sqrt(25));
        System.out.println(Math.pow(5, 2));
    }    
}

// There is no need to import Math class 
// Because it is a part of a Java.Lang package 
// which is automatically imported by the Java compiler into every source file