public class pra6_5 {
    public static void main(String[] args) {
        char ch1 = 'A';
        char ch2 = '9';
        char ch3 = 'b';
        char ch4 = ' ';

        System.out.println(Character.isDigit(ch2)); // true
        System.out.println(Character.isLetter(ch3)); // true
        System.out.println(Character.isWhitespace(ch4)); // true
        System.out.println(Character.isUpperCase(ch1)); // true
        System.out.println(Character.isLowerCase(ch3));  //true
        
    }
}
