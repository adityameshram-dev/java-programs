public class pra3_4 {
    public static void main(String[] args) {
        int i;
        for (i=1;i<=20;i++) {
            System.out.println(i);
        }
        i=1;
        while(i<=20) {
            System.out.println(i);
            i++;
        }
        i=1;
        do {
           System.out.println(i);
           i++;
        }while(i<=20);

    }
}
